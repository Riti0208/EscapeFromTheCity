bool CCollision::CircleCollision(D3DXVECTOR3 *pointPos, D3DXVECTOR3 *circleCenter, float fCircleRadius){
	if ((pointPos->x - circleCenter->x) * (pointPos->x - circleCenter->x) +
		(pointPos->z - circleCenter->z) * (pointPos->z - circleCenter->z) <= fCircleRadius * fCircleRadius)
		return true;
	return false;
}
