CMode *CMode::currentMode;
/*******************************************************************************
* 関数名:void Set()
*
* 引数:  void,なし
* 戻り値:なし
* 説明 モードの変更
*
*******************************************************************************/
CMode *CMode::Set(CMode *pMode){
	if (currentMode != NULL) {
		currentMode->Uninit();
		delete currentMode;
	}
	currentMode = pMode;
	currentMode->Init();
	return currentMode;
}
/*******************************************************************************
* 関数名:void Get()
*
* 引数:  void,なし
* 戻り値:なし
* 説明 モードの変更
*
*******************************************************************************/
CMode *CMode::Get(void){
	return currentMode;
}